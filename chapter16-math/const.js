// fundamental constants
console.log(Math.E); // the root of the natural logarithm: ~2.718
console.log(Math.PI); // the ratio of a circle's circumference to its diameter: ~3.142
// logarithmic convenience constants -- these can be accessed through library
// calls, but they're commonly used enough to warrant convenience constants
console.log(Math.LN2); // the natural logarithm of 2: ~0.693 
console.log(Math.LN10); // the natural logarithm of 10: ~2.303
console.log(Math.LOG2E); // the base 2 logarithm of Math.E: ~1.433 
console.log(Math.LOG10E); // the base 10 logarithm of Math.E: 0.434
// algebraic convenience constants
console.log(Math.SQRT1_2); // the square root of 1/2: ~0.707 Math.SQRT2 // the square root of 2: ~1.414